nrcBlocks = [[2..5],[6..8]]

nrcBl :: Int -> [Int]
nrcBl x = concat $ filter (elem x) nrcBlocks 

nrcGrid :: Sudoku -> (Row,Column) -> [Value]
nrcGrid s (r,c) = 
  [ s (r',c') | r' <- bl r, c' <- bl c ]

nrcInjective :: Sudoku -> (Row, Column) -> Bool
nrcInjective s (r,c) = injective vs where 
    vs = filter (/= 0) (nrcGrid s (r,c))

